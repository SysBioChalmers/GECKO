function [ecModel, rmseTrace, kcatTrace, sigmaLogTrace, diagnostics] = bayesianSensitivityTuning(ecModel,kcatSigmaLog,modelAdapter)
% bayesianSensitivityTuning
%   Performs an iterative ABC-SMC Bayesian parameter‑estimation procedure
%   that searches for kcat values which allow an ecModel to match
%   experimental flux data. It repeatedly proposes new sets of kcats,
%   simulates the ecModel with each set, measures the discrepancy (RMSE)
%   between predicted and observed growth/fluxes, and then keeps only the
%   best‑performing samples. Over generations, it gradually narrows the
%   acceptable region and adapts the proposal distribution—learning
%   correlations between kcats—until it converges on a posterior
%   distribution of plausible kcat values.
%
% Input:
%   ecModel         ecModel that was generated by makeEcModel, or loaded from
%                   an earlier run. Not compatible with ecModels generated by
%                   earlier GECKO versions (pre 3.0).
%   kcatSigmaLog    vector (same length as ecModel.ec.kcat) with the
%                   initial **log-space standard deviations** (sigmaLog)
%                   for kcats. By default, kcatSigmaLog is set from params.
%                   bayesian.initSigmaLogDefault and optionally overridden
%                   per source by params.bayesian.initSigmaLog (a vector of
%                   sigmaLog values aligned with params.bayesian.
%                   kcatSources). A custom kcatSigmaLog may also be
%                   provided.
%   modelAdapter    a loaded model adapter (Optional, will otherwise use the
%                   default model adapter).
%
% Output:
%   model           ecModel with new kcats, not applied.
%   rmseTrace       history of best RMSE
%   kcatTrace       history of posterior kcat means
%   sigmaLogTrace   history of posterior sigmaLog per generation
%
if nargin < 3 || isempty(modelAdapter)
    modelAdapter = ModelAdapterManager.getDefault();
    if isempty(modelAdapter)
        error('Either send in a modelAdapter or set the default ecModel adapter in the ModelAdapterManager.')
    end
end
%% Load hyperparameters
params = modelAdapter.params;
% Define initial kcat distributions
sigma0logDefault     = params.bayesian.sigma0logDefault;    % Default initial relative SD for kcat
kcatSources          = params.bayesian.kcatSources;         % List of annotation sources with custom SD multipliers
sigma0logSource      = params.bayesian.sigma0logSource;     % Per-source SD multipliers
shrinkThrDefault     = params.bayesian.shrinkThrDefault;
shrinkThrSource      = params.bayesian.shrinkThrSource;
varianceCapDefault   = params.bayesian.varianceCapDefault;
varianceCapSource    = params.bayesian.varianceCapSource;
forcePriorThrDefault = params.bayesian.forcePriorThrDefault;
forcePriorThrSource  = params.bayesian.forcePriorThrSource;

sparsityThr          = params.bayesian.sparsityThreshold;  % Sparsity threshold

% Number of samples per generation
scheduleGenerations  = params.bayesian.scheduleGenerations; % Increasing generations at which sample size changes
scheduleSamples      = params.bayesian.scheduleSamples;     % Sample counts corresponding to scheduleGenerations
% Which sampled models should be selected
targetAccept         = params.bayesian.targetAccept;        % RMSE percentile threshold (epsilon) for ABC acceptance
minKeep              = params.bayesian.minKeep;             % Min fraction of samples kept each generation
maxKeep              = params.bayesian.maxKeep;             % Max fraction of samples kept each generation 

rmseThreshold       = params.bayesian.rmseThreshold;       % Stop when RMSE reaches this level
maxGenerations      = params.bayesian.maxGenerations;      % Hard cap on the number of ABC–SMC generations

% Fixed parameters
alpha           = 0.65;
tauResidual     = 0.2;
sigmaFloorFrac  = 0.15;
adaptFracEarly  = 0.5;
cExpl_base      = 3;    cExpl_boost = 6;
boostThreshold  = 0.01;
plateau_trigger = 8;    plateau_duration = 5;
plateau_count   = 0;    boost_count = 0;
rMax            = 150;

% Keep track of the kcat source
kcatSourceIdx = zeros(size(ecModel.ec.kcat));
for i   = 1:numel(kcatSources)
    idx = strcmpi(ecModel.ec.source, kcatSources{i});
    kcatSourceIdx(idx) = i;
end
uniqKcatParams = find(kcatSourceIdx);
noKcatSource   = find(kcatSourceIdx == 0);

%%  Construct initial kcat variances based on annotation confidence
if nargin < 2 || isempty(kcatSigmaLog)
    sigma0log                 = sigma0logDefault * ones(size(ecModel.ec.kcat));
    sigma0log(uniqKcatParams) = sigma0logSource(kcatSourceIdx(uniqKcatParams));
end

%% Load experimental data and pre-computed indicators
bayData = loadBayesianData(modelAdapter);

% Add carbon counts for exchange reactions if missing (used for RMSE weighting)
if ~isfield(ecModel,'excarbon')
    ecModel = addCarbonNum(ecModel);
    ecModel.excarbon(ecModel.excarbon == 0) = 1;
end

%% Initialize diagnostic tracking
diagnostics = struct(); D = numel(ecModel.ec.kcat);

% Pre-allocate traces
diagnostics.shrinkageTrace = zeros(D, maxGenerations);           % Per-param shrinkage weights
diagnostics.acceptanceRateTrace = zeros(1, maxGenerations);      % Fraction accepted each gen
diagnostics.epsilonTrace = zeros(1, maxGenerations);             % ABC threshold
diagnostics.nSamplesTrace = zeros(1, maxGenerations);            % N proposals per gen
diagnostics.nAcceptedTrace = zeros(1, maxGenerations);           % N accepted per gen

% Per-source metrics
diagnostics.activeBySource = zeros(numel(kcatSources)+1, maxGenerations);  % Active params by source
diagnostics.nearPriorBySource = zeros(numel(kcatSources)+1, maxGenerations); % Near prior by source
diagnostics.meanDeviationBySource = zeros(numel(kcatSources)+1, maxGenerations); % Mean deviation
diagnostics.varianceRatioBySource = zeros(numel(kcatSources)+1, maxGenerations); % Posterior/prior var

% Sparsity metrics
diagnostics.sparsityCountTrace = zeros(1, maxGenerations);       % N params forced to prior
diagnostics.sparsityFractionTrace = zeros(1, maxGenerations);    % Fraction forced to prior

% Diversity metrics
diagnostics.diversityTrace = zeros(1, maxGenerations);           % Parameter space spread
diagnostics.meanAcceptedRMSE = zeros(1, maxGenerations);         % Mean of accepted (not just best)
diagnostics.medianAcceptedRMSE = zeros(1, maxGenerations);       % Median of accepted

% Proposal adaptation metrics
diagnostics.proposalWidthTrace = zeros(1, maxGenerations);       % Mean proposal sigma
diagnostics.lowRankDimTrace = zeros(1, maxGenerations);          % Rank of covariance structure

%% Prepare for ABC–SMC
%  - Compute RMSE of prior parameters (baseline)
%  - Initialize accepted samples with prior values
%  - Preallocate traces for diagnostics and convergence plots
kcats = ecModel.ec.kcat; kcat0 = kcats;

rmse = abc_max(ecModel, bayData, modelAdapter);
fprintf('RMSE with prior kcats: %.2f.\n', rmse)

rmseTop   = rmse;      % Best RMSE so far (accepted)
kcatTop   = kcats;     % Accepted kcat samples
rmseTrace = rmse;      % History of best RMSE across generations
kcatTrace = kcats;     % History of posterior means per generation
sigmaLogTrace  = sigma0log;   % History of posterior SDs per generation

PB1 = ProgressBar2(maxGenerations, ['Run through ' num2str(maxGenerations) ' generations'], 'gui');

% Fix carbon source; will be modified during sampling
tmpModel = setParam(ecModel,'lb',modelAdapter.params.c_source,0);

%% Proposal initialization (log-space)
eigVals = []; U = [];        % No low-rank information for generation 1

%% ABC–SMC LOOP
%  Each generation narrows the discrepancy threshold (epsilon), focusing the
%  posterior while simultaneously adapting the proposal to match the shape
%  of accepted samples (low-rank covariance structure).
generation = 1;
while rmse > rmseThreshold
    if generation <= maxGenerations
        fprintf('Running iteration %d of %d. ', generation, maxGenerations)

        schedulePos = find(generation >= scheduleGenerations, 1, 'last');
        N           = scheduleSamples(schedulePos);
        newRmse     = zeros(N,1);

        %% Draw new kcat samples
        %  1st generation: lognormal sampling around prior
        %  Later: adaptive low-rank mixture proposals based on accepted samples
        if generation == 1
            % Simple per-parameter sampling assuming lognormal prior
            muLog = log(kcats) - 0.5 .* (sigma0log .^ 2);
            muMat  = repmat(muLog, 1, N);
            sigMat = repmat(sigma0log, 1, N);
            randomKcats = lognrnd(muMat, sigMat);
        else
            % Draw parents with minimal duplication
            nParents = size(kcatTop, 2);

            if N <= nParents
                % We have more parents than needed → sample without replacement
                parent_idx = randperm(nParents, N);
            else
                % We need more samples than parents → use each parent at least once
                % then sample remainder uniformly
                nReps = floor(N / nParents);       % How many times to use each parent
                nRemainder = N - nReps * nParents; % How many extra samples needed

                % Use each parent nReps times, then sample nRemainder more
                base_idx = repmat(1:nParents, 1, nReps);
                extra_idx = randperm(nParents, nRemainder);
                parent_idx = [base_idx, extra_idx];

                % Shuffle to avoid any ordering bias
                parent_idx = parent_idx(randperm(N));
            end

            parents = kcatTop(:, parent_idx);
            randomKcats = proposeLowRankMixture(parents, U, eigVals, ...
                sigmaProp_log, sigma0log, tauResidual, alpha, cExpl);
        end

        % Guarantee positivity (log-space sampling can underflow)
        if any(randomKcats <= 0, 'all')
            warning('randomKcats contains non-positive values after sampling; enforcing floor.')
            randomKcats = max(randomKcats, realmin);
        end

        %% Evaluate RMSE for each proposal (forward simulations)
        PB2 = ProgressBar2(N, ['Simulate ' num2str(N) ' models with random kcats'], 'gui');
        parfor j = 1:N
            %Uncomment for Vera, remove before release
            addpath('/apps/Arch/software/Gurobi/11.0.2-GCCcore-12.3.0/matlab/')
            ecModelIter         = tmpModel;
            ecModelIter.ec.kcat = randomKcats(:,j);
            ecModelIter         = applyKcatConstraints(ecModelIter);
            newRmse(j)          = abc_max(ecModelIter, bayData, modelAdapter);
            count(PB2)
        end

        % Remove unsolvable models (RMSE = 0 often signals infeasibility)
        zeroRmse                = newRmse == 0;
        newRmse(zeroRmse)       = [];
        randomKcats(:,zeroRmse) = [];

        % Combine with previous accepted samples (SMC step)
        rmse = [newRmse; rmseTop];
        kcat = [randomKcats, kcatTop];

        %% Acceptance step: ABC–SMC epsilon thresholding
        epsilon = prctile(rmse, targetAccept);
        acc_idx = find(rmse <= epsilon); % Selected acceptable samples

        % Ensure at least minKeep and at most maxKeep of samples survive
        minCount = max(1, floor(minKeep * numel(rmse)));
        maxCount = max(1, floor(maxKeep * numel(rmse)));
        if numel(acc_idx) < minCount
            [~, ord] = sort(rmse, 'ascend');
            acc_idx = ord(1:minCount);
            epsilon = rmse(acc_idx(end));  % Update epsilon to actual threshold
        elseif numel(acc_idx) > maxCount
            [~,ord] = sort(rmse(acc_idx), 'ascend');
            acc_idx = acc_idx(ord(1:maxCount));
        end

        % Keep only accepted samples
        rmseTop = rmse(acc_idx);
        kcatTop = kcat(:, acc_idx);

        %% Update posterior kcat and sigmaLog
        logKcatTop      = log(kcatTop);
        muLog           = mean(logKcatTop, 2);
        kcatSigmaLog    = std(logKcatTop, 1, 2);

        % Compute deviation from prior
        devFromPrior = abs(muLog - log(kcat0)) ./ sigma0log;

        % Source-specific shrinkage thresholds
        shrinkWeight = min(devFromPrior / shrinkThrDefault, 1);
        for i = 1:numel(kcatSources)
            sourceIdx = kcatSourceIdx == i;
            shrinkWeight(sourceIdx) = min(devFromPrior(sourceIdx) / shrinkThrSource(i), 1);
        end

        forceToExactPrior = false(size(devFromPrior));

        % Apply source-specific force-to-prior thresholds
        for i = 1:numel(kcatSources)
            sourceIdx = (kcatSourceIdx == i);
            if any(sourceIdx) && forcePriorThrSource(i) > 0 % Only apply if threshold is positive
                % If deviation < threshold, mark for forcing to prior
                forceToExactPrior(sourceIdx) = (devFromPrior(sourceIdx) < forcePriorThrSource(i));
            end
        end

        % Apply default threshold to unlabeled sources
        if any(noKcatSource) && forcePriorThrDefault > 0
            forceToExactPrior(noKcatSource) = (devFromPrior(noKcatSource) < forcePriorThrDefault);
        end

        % Override shrinkWeight to 0 for forced parameters (= 100% prior, 0% posterior)
        shrinkWeight(forceToExactPrior) = 0;

        % Bayesian update
        kcatSigmaLog_raw = shrinkWeight .* kcatSigmaLog + (1 - shrinkWeight) .* sigma0log;
        kcats_raw = exp(shrinkWeight .* muLog + (1 - shrinkWeight) .* log(kcat0));

        % Sparsity enforcement: force small changes back to prior
        smalldiff        = abs(log(kcats_raw) - log(kcat0)) < sparsityThr * sigma0log;
        kcats            = kcats_raw;
        kcats(smalldiff) = kcat0(smalldiff);

        kcatSigmaLog = kcatSigmaLog_raw;
        kcatSigmaLog(smalldiff) = sigma0log(smalldiff);

        % Cap variance growth by source to prevent random walk
        for i = 1:numel(kcatSources)
            sourceIdx = kcatSourceIdx == i;
            kcatSigmaLog(sourceIdx) = min(kcatSigmaLog(sourceIdx), sigma0log(sourceIdx) * varianceCapSource(i));
        end
        kcatSigmaLog(noKcatSource)  = min(kcatSigmaLog(noKcatSource), sigma0log(noKcatSource) * varianceCapDefault);

        %% Learn proposal covariance structure from accepted samples
        % For use in next sampling round
        [U, eigVals, sigmaProp_log] = buildLowRankLogProposal(log(kcatTop), rMax, ...
            sigma0log, sigmaFloorFrac, adaptFracEarly);

        % Track trace of progress and posterior contraction
        medianRMSE = median(rmseTop);
        rmseTrace  = [rmseTrace, medianRMSE];
        kcatTrace  = [kcatTrace, kcats];
        sigmaLogTrace  = [sigmaLogTrace,  kcatSigmaLog];

        % Use median of accepted samples as center point for next generation
        tmpModel.ec.kcat = median(kcatTop, 2);
        tmpModel = applyKcatConstraints(tmpModel);

        %% Boost exploration
        if generation > 1
            % Count stagnant generations
            if (rmseTrace(end-1) - rmseTrace(end)) / rmseTrace(end-1) < boostThreshold
                plateau_count = plateau_count + 1;
            else
                plateau_count = 0;
            end

            % Boost if stuck for 10 gens
            if plateau_count >= plateau_trigger && boost_count == 0
                boost_count = plateau_duration;
                fprintf('    ⚡ Boosting exploration\n');
            end

            % Apply boost
            cExpl = (boost_count > 0) * cExpl_boost + (boost_count == 0) * cExpl_base;
            boost_count = max(0, boost_count - 1);
        end

        %% Store diagnostics
        % Store shrinkage weights
        diagnostics.shrinkageTrace(:, generation) = shrinkWeight;

        % Store acceptance metrics
        diagnostics.acceptanceRateTrace(generation) = numel(rmseTop) / numel(rmse);
        diagnostics.epsilonTrace(generation)        = epsilon;
        diagnostics.nSamplesTrace(generation)       = N;
        diagnostics.nAcceptedTrace(generation)      = numel(rmseTop);

        % Store RMSE statistics
        diagnostics.meanAcceptedRMSE(generation)    = mean(rmseTop);
        diagnostics.medianAcceptedRMSE(generation)  = median(rmseTop);

        % Store sparsity metrics
        diagnostics.sparsityCountTrace(generation)  = sum(smalldiff);  % From sparsity code
        diagnostics.sparsityFractionTrace(generation) = sum(smalldiff) / D;

        diagnostics = storeSourceMetrics(diagnostics, generation, shrinkWeight, ...
            kcatSigmaLog, kcats, kcat0, sigma0log, kcatSourceIdx, ...
            kcatSources, noKcatSource);

        % Store diversity metric (spread in log parameter space)
        logKcatRanges = max(log(kcatTop), [], 2) - min(log(kcatTop), [], 2);
        diagnostics.diversityTrace(generation) = mean(logKcatRanges);

        % Store proposal width
        diagnostics.proposalWidthTrace(generation) = mean(sigmaProp_log);

        % Store low-rank dimension
        diagnostics.lowRankDimTrace(generation) = size(U, 2);
        printGenerationSummary(generation, maxGenerations, rmseTrace, ...
            shrinkWeight, rmseTop, rmse, sum(smalldiff), D, ...
            ecModel, kcatSigmaLog, sigma0log, kcatSources)
        generation = generation + 1;
        count(PB1)
    else
        fprintf('Halted due to reaching maximum generation limit. Returning best posterior kcats.\n')
        break
    end
end

%% Add summary statistics
diagnostics.finalGeneration = generation;
diagnostics.converged       = rmseTrace(end) < rmseThreshold;
diagnostics.sourceLabels    = [kcatSources; {'unlabelled'}];

%% Return median posterior parameters from the last accepted population
medianKcats_final = median(kcatTop, 2);  % Element-wise median across all accepted samples
ecModel.ec.kcat   = medianKcats_final;
ecModel           = applyKcatConstraints(ecModel);

% Report median RMSE of accepted samples
medianRMSE_final = median(rmseTop);
fprintf('Final median RMSE: %.2f (range: %.2f - %.2f from %d samples).\n', ...
    medianRMSE_final, min(rmseTop), max(rmseTop), numel(rmseTop))
end

%% Helpers
function proposals = proposeLowRankMixture(parents, U, eigVals, sigmaProp_log, sigma0log, tau, alpha, cExpl)
% proposeLowRankMixture
%   Draw proposals in log-space using a *mixture* of two kernels:
%
%   1) Exploit (probability alpha):
%      - Uses a learned *low-rank* structure (U, eigVals) that captures the
%        dominant posterior directions (from PCA/SVD in log-space).
%      - Adds an isotropic residual component scaled by sqrt(tau) to avoid
%        moving only along the low-rank subspace.
%      - Applies per-parameter marginal scales sigmaProp_log (log-space).
%
%   2) Explore (probability 1 - alpha):
%      - Uses a broad *diagonal* baseline (no correlations) based on the
%        initial/baseline scale sigma0log, inflated by cExpl to enable
%        occasional large moves that help escape local modes.
%
%   Both kernels are *centered at the parent point in log-space*.
%   We return proposals in the *original (linear) space* (i.e., exponentiated).
%
% INPUTS
%   parents        [D x Nprop] current points to branch from; strictly positive
%                   parameters (we transform via log for proposals).
%   U              [D x r] orthonormal columns (low-rank basis) learned in log-space.
%   eigVals         [r x 1] variances along columns of U in log-space.
%   sigmaProp_log  [D x 1] per-parameter proposal stds in log-space for the
%                   exploit kernel (often adapted early, then frozen).
%   sigma0log     [D x 1] baseline per-parameter stds in log-space for explore kernel.
%   tau            Scalar >= 0, residual isotropic noise multiplier within the
%                   exploit kernel (sqrt(tau) scales the residual white noise).
%   alpha          Mixture weight in [0,1]: probability of using the exploit kernel.
%   cExpl          Scalar >= 1, inflation for the explore kernel (broadens steps).
%
% OUTPUT
%   proposals      [D x Nprop] proposed points in *linear* space; each column
%                  corresponds to the same column of 'parents'.
%
% RATIONALE (Bayesian/MCMC intuition)
%   - The exploit kernel proposes along the principal directions of posterior
%     variability (U, eigVals), encouraging efficient moves with good acceptance.
%   - The explore kernel ensures global mixing: occasional broader, unstructured
%     jumps reduce the risk of getting stuck (multi-modality, narrow valleys).
%   - Mixing the two (with alpha) balances local efficiency and global robustness.

% Basic sizes and the number of low-rank directions
[D, Nprop] = size(parents);   % D: number of parameters, Nprop: number of proposals to draw
r = size(U, 2);               % r: low-rank dimensionality (can be zero)

% Randomly choose which proposals use exploit vs explore kernel
%   - Each column (proposal) independently picks exploit with prob alpha
%   - Logical index for columns assigned to exploit
useExploit = rand(1, Nprop) < alpha;

% ============================= EXPLOIT KERNEL ============================
% Form:  step_log = diag(sigmaProp_log) * ( sqrt(tau)*E  +  U*diag(sqrt(eigVals))*Z )
% where E ~ N(0, I_D), Z ~ N(0, I_r), independent.
%
% Interpretation:
%   - U*diag(sqrt(eigVals))*Z: structured move in principal directions (low-rank).
%   - sqrt(tau)*E: residual white noise (prevents sticking strictly to U's subspace).
%   - diag(sigmaProp_log): per-parameter scaling in log-space (adapted/frozen magnitude).

nE = sum(useExploit);  % number of exploit proposals to generate
if nE > 0
    % Random draws for structured and residual components
    Zr = randn(r, nE);     % low-rank standard normals
    Er = randn(D, nE);     % residual white noise

    % Structured + residual direction (before per-parameter scaling)
    if r > 0
        % U * (sqrt(eigVals) .* Zr): project random vector in low-rank basis
        y_exploit = bsxfun(@times, sqrt(tau), Er) + U * (bsxfun(@times, sqrt(eigVals), Zr));
    else
        % If no learned directions yet, rely only on residual term
        y_exploit = bsxfun(@times, sqrt(tau), Er);
    end

    % Apply per-parameter magnitudes in log-space
    steps_exploit = bsxfun(@times, sigmaProp_log, y_exploit);  % [D x nE]
else
    steps_exploit = zeros(D, 0);  % placeholder when no exploit draws
end
% =========================================================================

% ============================= EXPLORE KERNEL ============================
% Form: step_log = diag(cExpl * sigma0log) * E,  with E ~ N(0, I_D).
%
% Interpretation:
%   - Diagonal, baseline scale in log-space (no correlations).
%   - Inflated by cExpl to ensure broader, occasional jumps for global mixing.

nX = sum(~useExploit);  % number of explore proposals to generate
if nX > 0
    Ee = randn(D, nX);  % white noise in log-space
    steps_explore = bsxfun(@times, cExpl * sigma0log, Ee);  % [D x nX]
else
    steps_explore = zeros(D, 0);  % placeholder when no explore draws
end
% =========================================================================

% Stitch the two blocks back to the original proposal order (columns)
steps = zeros(D, Nprop);
if nE > 0, steps(:,  useExploit) = steps_exploit; end
if nX > 0, steps(:, ~useExploit) = steps_explore; end

% Convert from log-space steps back to linear space proposals:
parents_pos = max(parents, realmin);         % ensure strictly positive
proposals   = exp(log(parents_pos) + steps); % apply additive step in log-space
proposals   = max(proposals, realmin);       % clamp against underflow to 0

% Truncate by realistic bounds
% 1e-2 = restriction endonucleases / 4e7 = catalase
proposals = max(min(proposals, 1e8), 1e-3);
end

function [U, eigVals, sigmaProp_log] = buildLowRankLogProposal( ...
    thetaAcc, rMax, sigma0log, sigmaFloorFrac, adaptFracEarly)
% buildLowRankLogProposal  Low-rank structure + scale for a log-space proposal.
%
% This function extracts dominant directions (a low-rank subspace) from the
% log-space accepted samples and computes a per-parameter sampling scale
% (std) for a proposal distribution.
%
% The SVD of mean-centered samples estimates principal directions of
% posterior variability; using only the top directions yields a robust
% low-rank approximation.
%
% Early in sampling, we adapt cautiously (blend observed std with a
% baseline). Later, we "freeze" scales to ensure stability.
%
% INPUTS
%   thetaAcc         [D x Nacc] matrix of accepted samples.
%                   If inputIsLog=false, these must be > 0 (we take log).
%   rMax            Maximum rank used for the low-rank subspace.
%   sigma0log      [D x 1] baseline (initial) std in log-space per parameter.
%   sigmaFloorFrac  Scalar >= 0; per-parameter floor as a fraction of sigma0log.
%   adaptFracEarly  (optional) in [0,1]; weight on observed std during early
%                   adaptation. Default = 0.5 (balanced blend).
%
% OUTPUTS
%   U               [D x rUse] orthonormal columns spanning dominant directions
%                   of variability in log-space (low-rank basis). Empty if rank=0.
%   eigVals          [rUse x 1] eigenvalues (variances) along columns of U in log-space.
%                   Empty if rank=0.
%   sigmaProp_log   [D x 1] per-parameter proposal std in log-space. Early on,
%                   a blend of observed std and baseline; after freeze, baseline.
%
[D, Nacc] = size(thetaAcc);

% Mean-center the accepted samples in log-space
Xc = thetaAcc - mean(thetaAcc, 2);

% Low-rank structure via SVD of the (scaled) centered matrix. Keep up to
% rMax (and available rank) to form a robust low-rank basis
if Nacc >= 2
    denom = sqrt(max(Nacc - 1, 1));
    [Ufull, S, ~] = svd(Xc / denom, 'econ'); % columns of Ufull are orthonormal
    rAvail = size(Ufull, 2);
    rUse   = min([rAvail, rMax, max(Nacc - 1, 1)]);
    if rUse >= 1
        U      = Ufull(:, 1:rUse);                  % dominant directions
        eigVals = (diag(S(1:rUse, 1:rUse))).^2;      % variances along U (log-space)
    else
        U = zeros(D, 0);
        eigVals = zeros(0, 1);
    end
else
    % Not enough samples to learn directions
    U = zeros(D, 0);
    eigVals = zeros(0, 1);
end

% Marginal proposal scales (per parameter) in log-space, blend observed std with baseline
stds_obs = std(thetaAcc, 1, 2);
sigmaProp_log = adaptFracEarly .* stds_obs + (1 - adaptFracEarly) .* sigma0log;
sigmaProp_log = max(sigmaProp_log, sigmaFloorFrac .* sigma0log);
end

function diagnostics = storeSourceMetrics(diagnostics, gen, shrinkWeight, ...
    kcatSigmaLog, kcats, kcat0, sigma0log, kcatSourceIdx, ...
    kcatSources, noKcatSource)
    
    % Per-source metrics
    for i = 1:numel(kcatSources)
        idx = (kcatSourceIdx == i);  % Faster than strcmpi
        if any(idx)
            diagnostics.activeBySource(i, gen) = sum(shrinkWeight(idx) > 0.3);
            diagnostics.nearPriorBySource(i, gen) = sum(abs(kcatSigmaLog(idx) - sigma0log(idx)) < 0.1);
            diagnostics.meanDeviationBySource(i, gen) = mean(abs(log(kcats(idx)) - log(kcat0(idx))) ./ sigma0log(idx));
            diagnostics.varianceRatioBySource(i, gen) = mean(kcatSigmaLog(idx) ./ sigma0log(idx));
        end
    end
    
    % "Other" category (last row)
    otherIdx = numel(kcatSources) + 1;
    diagnostics.activeBySource(otherIdx, gen) = sum(shrinkWeight(noKcatSource) > 0.3);
    diagnostics.nearPriorBySource(otherIdx, gen) = sum(abs(kcatSigmaLog(noKcatSource) - sigma0log(noKcatSource)) < 0.1);
    diagnostics.meanDeviationBySource(otherIdx, gen) = mean(abs(log(kcats(noKcatSource)) - log(kcat0(noKcatSource))) ./ sigma0log(noKcatSource));
    diagnostics.varianceRatioBySource(otherIdx, gen) = mean(kcatSigmaLog(noKcatSource) ./ sigma0log(noKcatSource));
end

function printGenerationSummary(generation, maxGenerations, rmseTrace, ...
    shrinkWeight, rmseTop, rmse, n_forced_prior, D, ...
    ecModel, kcatSigmaLog, sigma0log, kcatSources)

acceptRate = numel(rmseTop) / numel(rmse);
numel_rmseTop = numel(rmseTop);
numel_rmse = numel(rmse);

% Compact one-liner
fprintf('Gen %2d/%2d │ RMSE: %5.2f →%5.2f (Δ%4.1f%%) │ Accept: %3d/%3d (%2.0f%%) │ Active: %4d (%2.0f%%) │ Sparse: %4d', ...
    generation, maxGenerations, ...
    rmseTrace(max(1, end-1)), rmseTrace(end), ...
    100 * (rmseTrace(max(1, end-1)) - rmseTrace(end)) / rmseTrace(max(1, end-1)), ...
    numel_rmseTop, numel_rmse, 100 * acceptRate, ...
    sum(shrinkWeight > 0.3), 100 * sum(shrinkWeight > 0.3) / D, ...
    n_forced_prior);

% Inline warnings
warnings = {};
if acceptRate > 0.5, warnings{end+1} = '⚠️ACC↑'; end
if acceptRate < 0.08, warnings{end+1} = '⚠️ACC↓'; end
if generation >= 5
    improvement = (rmseTrace(end-4) - rmseTrace(end)) / rmseTrace(end-4);
    if improvement < 0.02, warnings{end+1} = '⚠️PLAT'; end
end
if ~isempty(warnings)
    fprintf(' │ %s', strjoin(warnings, ' '));
end
fprintf('\n');

% Detailed every 10 gens
if mod(generation, 10) == 0
    fprintf('├─────────────────────────────────────────────────────────────────────────────\n');
    fprintf('│ SOURCE BREAKDOWN:\n');
    
    for i = 1:numel(kcatSources)+1
        if i>numel(kcatSources)
            idx = ~ismember(ecModel.ec.source, kcatSources);
            sourceName = 'OTHERS';
        else
            idx = strcmpi(ecModel.ec.source, kcatSources{i});
            sourceName = upper(kcatSources{i});
        end

        if any(idx)
            n_total = sum(idx);
            n_active = sum(shrinkWeight(idx) > 0.3);
            n_near_prior = sum(abs(kcatSigmaLog(idx) - sigma0log(idx)) < 0.1);
            mean_var = mean(kcatSigmaLog(idx) ./ sigma0log(idx));
            
            fprintf('│   %-8s: %4d active (%2.0f%%) │ %4d near prior (%2.0f%%) │ σ: %.1fx  ', ...
                sourceName, n_active, 100*n_active/n_total, ...
                n_near_prior, 100*n_near_prior/n_total, mean_var);
            
            if (i == 1 && n_near_prior/n_total > 0.5) || ...
               (i == 2 && n_active/n_total > 0.3) || ...
               (i == 3 && n_near_prior/n_total > 0.7)
                fprintf('✓\n');
            else
                fprintf('⚠️\n');
            end
        end
    end
    
    if generation >= 10
        imp10 = 100 * (rmseTrace(end-9) - rmseTrace(end)) / rmseTrace(end-9);
        fprintf('│ 10-gen improvement: %.1f%%\n', imp10);
    end
    fprintf('└─────────────────────────────────────────────────────────────────────────────\n');
end
end
